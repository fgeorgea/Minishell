#include "../minishell.h"

void	create_config_str(void)
{
	g_sh->config_file = ft_strjoin(get_env_value("HOME"), "/.minishellrc");
	if (!g_sh->config_file)
		ft_exit(EXIT_MALLOC_FAILURE);
}

