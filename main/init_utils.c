#include "../minishell.h"

void	create_config_str(void)
{
	char	*tmp;

	tmp = get_env_value("HOME");
	if (!tmp)
		return ;
	g_sh->config_file = ft_strjoin(tmp, "/.minishellrc");
	if (!g_sh->config_file)
		ft_exit(EXIT_MALLOC_FAILURE);
}

